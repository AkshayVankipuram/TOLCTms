default = '#4682b4'

def get_palettes():
    return list(colorbrewer.keys())

def get_color(palette):
    if palette in colorbrewer:
        return colorbrewer[palette]
    else:
        return [default]

def get_color_at_depth(palette, depth):
    if palette in colorbrewer:
        if depth < len(colorbrewer[palette]):
            return colorbrewer[palette][depth]
    return default
